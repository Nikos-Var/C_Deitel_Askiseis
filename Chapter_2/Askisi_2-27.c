#include <stdio.h>

int main() {
    int x, y;

    printf("Δώστε δύο θετικους ακεραίους: \n");
    scanf("%d %d", &x, &y);

    if (x % y == 0) {
        printf("Ο αριθμός %d είναι πολλαπλάσιος του %d \n", x, y);
    }

    if (x % y != 0) {
        printf("Ο αριθμός %d δεν είναι πολλαπλάσιος του %d \n", x, y);
    }
}