#include <stdio.h>

int main() {
    int x;

    printf("Δώστε έναν θετικό ακέραιο: \n");
    scanf("%d", &x);

    if (x % 2 == 0) {
    printf("Ο αριθμός %d είναι άρτιος.\n", x);
    }
    if (x % 2 != 0) {
    printf("Ο αριθμός %d είναι περιτός.\n", x);
    }
}