#include <stdio.h>

int x, y;

int main (void) {
    
    printf("Δώστε 2 ακεραίους αριθμούς: \n");
    scanf("%d%d", &x, &y);

    printf("Το άθροισμα των 2 αριθμών είναι: %d \n", x + y);
    printf("Το γινόμενο των 2 αριθμών είναι: %d \n", x * y);
    printf("Η διαφορά των 2 αριθμών είναι: %d \n", x - y);
    printf("Το πηλίκο των 2 αριθμών είναι: %d \n", x / y);
    printf("Το υπόλοιπο διαίρεσης των 2 αριθμών είναι: %d \n", x % y);
    
}